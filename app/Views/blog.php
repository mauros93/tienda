        <h5 class="card-title">El Transistor</h5>
        <p class="card-text">Es muy probable que a la gran mayoría de quienes lean esto, no reconozcan los nombres de John Bardeen, Walter Brattain y William Shockley. Sin embargo, pocas personas han influido de manera tan decisiva en nuestro modo de vida como esos tres científicos. A los tres debemos la invención del transistor, sin el cual la vida cotidiana sería sustancialmente diferente a como la conocemos actualmente. Los transistores son dispositivos que controlan el movimiento de los electrones y, en consecuencia, de la corriente eléctrica. Estos disponen de tres terminales denominadas emisor, base y colector.
        Su funcionamiento se puede comparar al de un grifo de agua. El agua que circula por una tubería (emisor) sale hacia la boca del grifo (colector) a través de una llave (base) que abre o cierra el flujo de agua controlando su caudal. Sería a finales de la década de los 40, cuando este equipo científicos conseguiría fabricar el primer prototipo. Para ello usaron un material semiconductor poco conocido hasta el momento: el germanio. Walter Brattain escribió en su cuaderno de laboratorio los detalles esenciales del dispositivo que propiciaría en los años posteriores una de las revoluciones silenciosas más importantes del siglo XX: había nacido el transistor. El transistor fue creado debido a la necesidad de remplazar los tubos vacío, los cuales eran artilugios muy poco fiables que además consumían mucha energía y esparcían mucho calor, que eran utilizados básicamente para amplificar las señales de comunicaciones en largas distancias.
        Hoy en día este simple pero esencial componente es la base de todo dispositivo electrónico, y hace parte de nuestra cotidianidad. Si no existiera el transistor, seguramente no estarías leyendo este artículo a través de una pantalla móvil o de computadora, pues es la base de la arquitectura de todos microchips y procesadores que utilizan los artículos electrónicos moderno; solo que nuestra tecnología ha avanzado a tal punto, que hemos logrado reducir el tamaño del transistor a medidas microscópicas. En Planet Express  sabemos mucho de esto. Sabemos cómo se conforman los sistemas desde su unidad de procesamiento más pequeña. Por ende tenemos los mejores procesadores y dispositivos de computo a tu disposición. Así que ven y visítanos. Seguramente encontrarás algo que no sabías que podía serte útil.</p>
